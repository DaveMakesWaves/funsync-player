  /**
   * Update the header's directory-path label to match the current view state.
   * Folder view tracks `_currentFolderPath`; flat view keeps the
   * collection-ancestor / source-summary behaviour from `show()`.
   */
  /**
   * Show / hide the "Searching in: ..." scope pill beneath the header. Only
   * surfaces when the user has an active query in folder-view and is drilled
   * into a folder — at root there's no "current folder" distinction to make.
   * Clicking the toggle flips `_searchGlobalOverride` and re-applies filters.
   */
  /**
   * Build a videoPath → [collectionName, categoryName, ...] map so
   * fuzzySearch can score hits against user-assigned context even when the
   * filename itself doesn't contain the query. Heavier than searching just
   * names, so only called when a search query is active.
   */
  _buildSearchContextMap() {
    const map = new Map();
    const add = (path, label) => {
      if (!path || !label) return;
      const list = map.get(path);
      if (list) list.push(label);
      else map.set(path, [label]);
    };

    const collections = this._settings.get('library.collections') || [];
    for (const col of collections) {
      if (!col?.name) continue;
      const paths = col.videoPaths || [];
      for (const p of paths) add(p, col.name);
    }

    const categories = this._settings.get('library.categories') || [];
    const videoCategories = this._settings.get('library.videoCategories') || {};
    const catById = new Map();
    for (const c of categories) if (c?.id) catById.set(c.id, c.name || '');
    for (const [videoPath, catIds] of Object.entries(videoCategories)) {
      if (!Array.isArray(catIds)) continue;
      for (const id of catIds) {
        const name = catById.get(id);
        if (name) add(videoPath, name);
      }
    }

    return map;
  }

