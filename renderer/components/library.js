  show(containerEl) {
    this._container = containerEl;

    // Wire the resize listener once per show. The cached `_vsColumns`
    // only updates inside `_renderGrid`; without this, a window resize
    // (tile → fullscreen, maximise, etc.) leaves virtual scroll using
    // the old column count → scroll goes haywire.
    if (!this._resizeListenerAttached) {
      window.addEventListener('resize', this._onResize);
      this._resizeListenerAttached = true;
    }

    // Resolve which directories to scan — all enabled sources, excluding unavailable (disconnected drives).
    // `library.directory` is a legacy singleton setting that app.js migrates into `library.sources` on
    // startup, so there's no need to fall back to it here.
    const sources = this._settings.get('library.sources') || [];
    const unavailable = this._unavailablePaths || new Set();
    const activeSources = sources.filter(s => s.enabled !== false && !unavailable.has(s.path));

    const sourceMap = {}; // path → name for VR content server grouping
    for (const s of activeSources) sourceMap[s.path] = s.name;
    const scanPaths = activeSources.map(s => s.path);

    this._sourceMap = sourceMap;
    this._dirPath = scanPaths.length > 0 ? scanPaths[0] : null;

    // Determine header title and subtitle based on active view
    const availableSources = sources.filter(s => s.enabled !== false && !unavailable.has(s.path));
    if (this._activeCollectionId) {
      const collections = this._settings.get('library.collections') || [];
      const col = collections.find(c => c.id === this._activeCollectionId);
      this._headerTitle = col ? col.name : 'Library';
      // Prefer the sync folder for synced collections (more informative
      // than the common ancestor of a possibly-empty additive list).
      // Fall back to common-ancestor of the frozen videoPaths for
      // legacy collections, then to the source summary.
      const syncFolder = col?.syncSource?.folderPath
        || (col?.syncSource?.sourceId
              ? sources.find(s => s.id === col.syncSource.sourceId)?.path
              : null);
      const ancestor = syncFolder
        || (col?.videoPaths?.length ? commonAncestorOfFiles(col.videoPaths) : '');
      if (ancestor) {
        this._headerPath = ancestor;
      } else {
        this._headerPath = availableSources.length === 1
          ? availableSources[0].path
          : `${availableSources.length} source${availableSources.length !== 1 ? 's' : ''}`;
      }
    } else {
      this._headerTitle = 'Library';
      this._headerPath = availableSources.length > 1
        ? availableSources.map(s => s.name).join(', ')
        : (this._dirPath || '');
    }

    // Check if we can skip re-scan (same paths, already have videos)
    const scanKey = scanPaths.sort().join('\0');
    const canSkip = this._lastScanKey === scanKey && this._videos.length > 0;

    if (scanPaths.length > 0) {
      this._renderWithHeader();
      if (canSkip) {
        // Re-render with cached videos (no re-scan needed)
        this._applyFilters();
      } else {
        this._lastScanKey = scanKey;
        this._scanDirectory(scanPaths.length === 1 ? scanPaths[0] : scanPaths, sourceMap);
      }
    } else {
      this._renderEmpty();
    }
  }

  /**
   * Force a full library rescan, busting the per-source `_lastScanKey`
   * cache that normally skips re-scans when the configured source
   * paths haven't changed. Wired to the header refresh button.
   *
   * Picks up files added to source folders since the last scan (e.g.
   * the user dropped a video into a sync-with-folder source via File
   * Explorer). Synced collections automatically refresh their
   * membership because `expandSyncedMembership` runs on every
   * `_applyFilters` pass.
   *
   * Idempotent — multiple clicks during an in-flight scan no-op
   * (`_scanning` flag gates the rescan).
   */
  async refresh() {
    if (this._scanning) return;
    const btn = this._container?.querySelector('.library__refresh-btn');
    if (btn) btn.classList.add('library__refresh-btn--spinning');
    const { showToast } = await import('../js/toast.js');
    const beforePaths = new Set(this._videos?.map(v => v.path) || []);

    // Re-resolve the scan paths the same way show() does (mirrors
    // lines 119–125 — sources filtered to enabled + available). Direct
    // scan-call path avoids re-rendering the entire header DOM, so the
    // refresh button reference stays valid for the spinner class.
    const sources = this._settings.get('library.sources') || [];
    const unavailable = this._unavailablePaths || new Set();
    const activeSources = sources.filter(s => s.enabled !== false && !unavailable.has(s.path));
    const sourceMap = {};
    for (const s of activeSources) sourceMap[s.path] = s.name;
    const scanPaths = activeSources.map(s => s.path);
    if (scanPaths.length === 0) {
      if (btn) btn.classList.remove('library__refresh-btn--spinning');
      showToast('No source folders to refresh', 'warn', 3000);
      return;
    }

    this._lastScanKey = scanPaths.sort().join('\0');
    try {
      await this._scanDirectory(scanPaths.length === 1 ? scanPaths[0] : scanPaths, sourceMap);
      const afterPaths = this._videos?.map(v => v.path) || [];
      const added = afterPaths.filter(p => !beforePaths.has(p)).length;
      const removed = [...beforePaths].filter(p => !afterPaths.includes(p)).length;
      if (added === 0 && removed === 0) {
        showToast('Library refreshed — no changes', 'info', 2500);
      } else {
        const parts = [];
        if (added > 0) parts.push(`${added} new video${added !== 1 ? 's' : ''}`);
        if (removed > 0) parts.push(`${removed} removed`);
        showToast(`Library refreshed — ${parts.join(', ')}`, 'info', 3000);
      }
    } catch (err) {
      console.error('[Library] refresh failed:', err);
      showToast(`Refresh failed: ${err?.message || 'unknown error'}`, 'error', 4000);
    } finally {
      if (btn) btn.classList.remove('library__refresh-btn--spinning');
    }
  }

