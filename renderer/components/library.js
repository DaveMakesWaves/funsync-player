  async _pinFolderAsCollection(node) {
    const paths = descendantsOf(this._folderIndex, node.path).map(v => v.path);
    if (paths.length === 0) {
      const { showToast } = await import('../js/toast.js');
      showToast('Folder is empty', 'warn');
      return;
    }
    const name = await Modal.prompt('Pin Folder as Collection', 'Collection name', node.label);
    if (!name) return;

    // Auto-enable sync-with-source. Pinning a folder has the same
    // intent as syncing: "this folder IS my collection, including any
    // new videos that land in it later". If the folder matches a
    // registered source, track by sourceId (survives path rename);
    // otherwise track by folderPath.
    const sources = this._settings.get('library.sources') || [];
    const canonical = canonicalPath(node.path);
    const matchingSource = sources.find(s => canonicalPath(s.path) === canonical);
    const syncSource = matchingSource
      ? { sourceId: matchingSource.id }
      : { folderPath: canonical };

    const collections = this._settings.get('library.collections') || [];
    const newCol = {
      id: crypto.randomUUID(),
      name,
      videoPaths: [],    // additive-include list (empty — all members come from sync)
      syncSource,
      excludedPaths: [],
    };
    collections.push(newCol);
    this._settings.set('library.collections', collections);

    const { showToast } = await import('../js/toast.js');
    showToast(`Pinned "${name}" — syncing ${paths.length} video${paths.length !== 1 ? 's' : ''} from folder`, 'info');
  }

