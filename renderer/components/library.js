  _togglePicker(pop, btn) {
    if (pop.hidden) this._openPicker(pop, btn); else this._closePicker(pop, btn);
  }
  _openPicker(pop, btn) {
    // Close any other open picker first (single-open-at-a-time policy
    // matching the web-remote sheet behaviour — keeps the mental model
    // simple per Norman conceptual model).
    for (const other of this._container.querySelectorAll('.library__picker-pop')) {
      if (other !== pop) {
        const otherBtn = this._container.querySelector(`[aria-controls="${other.id}"]`)
          || this._container.querySelector(`#${other.id.replace('-pop', '-btn')}`);
        this._closePicker(other, otherBtn);
      }
    }
    pop.hidden = false;
    btn.setAttribute('aria-expanded', 'true');
  }
  _closePicker(pop, btn) {
    if (pop.hidden) return;
    pop.hidden = true;
    if (btn) btn.setAttribute('aria-expanded', 'false');
  }
  _wirePickerDismissal(pop, btn) {
    // Outside-click dismissal. Single document-level listener per
    // picker — listeners auto-tear-down on header rebuild because
    // the elements are GC'd when innerHTML reassigns.
    const handler = (e) => {
      if (pop.hidden) return;
      if (pop.contains(e.target) || btn.contains(e.target)) return;
      this._closePicker(pop, btn);
    };
    document.addEventListener('click', handler, { capture: true });
    pop.addEventListener('keydown', (e) => {
      if (e.key === 'Escape') {
        e.stopPropagation();
        this._closePicker(pop, btn);
        btn.focus();
      }
    });
  }

