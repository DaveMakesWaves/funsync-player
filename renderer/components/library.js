  /**
   * Cycle the manual VR classification for a single video.
   *
   * State machine (matches kebab item wording):
   *   no override + heuristic flat  → set 'vr'   ("Mark as VR")
   *   no override + heuristic VR    → set 'flat' ("Mark as Flat")
   *   override 'vr'                 → unset      ("Reset VR")
   *   override 'flat'               → unset      ("Reset VR")
   *
   * Override beats heuristic in `isVRVideo()`, so flipping or clearing
   * here is enough to update the VR filter, the badge, and (after the
   * web-remote pulls fresh videos) the phone-side classification.
   */
  async _toggleVRType(video) {
    if (!video || !video.path) return;
    const map = { ...(this._settings.get('library.manualVRType') || {}) };
    const current = map[video.path] || null;

    let next;
    if (current === 'vr' || current === 'flat') {
      delete map[video.path];
      next = null;
    } else {
      next = isVRVideo(video.path) ? 'flat' : 'vr';
      map[video.path] = next;
    }
    this._settings.set('library.manualVRType', map);

    this._applyFilters();

    const { showToast } = await import('../js/toast.js');
    if (next === 'vr') showToast('Marked as VR', 'info', 1800);
    else if (next === 'flat') showToast('Marked as Flat', 'info', 1800);
    else showToast('VR classification reset', 'info', 1800);
  }

  /**
   * Bulk variant of `_toggleVRType` for the multi-select toolbar.
   * `mode` is `'vr'`, `'flat'`, or `'reset'`. Writes once and re-renders
   * once at the end, regardless of how many paths are touched.
   */
  async _bulkSetVRType(paths, mode) {
    if (!Array.isArray(paths) || paths.length === 0) return;
    const map = { ...(this._settings.get('library.manualVRType') || {}) };
    let changed = 0;
    for (const p of paths) {
      if (!p) continue;
      if (mode === 'reset') {
        if (p in map) { delete map[p]; changed++; }
      } else if (mode === 'vr' || mode === 'flat') {
        if (map[p] !== mode) { map[p] = mode; changed++; }
      }
    }
    this._settings.set('library.manualVRType', map);
    this._applyFilters();

    const { showToast } = await import('../js/toast.js');
    const noun = paths.length === 1 ? 'video' : 'videos';
    if (mode === 'vr') showToast(`Marked ${paths.length} ${noun} as VR`, 'info', 2000);
    else if (mode === 'flat') showToast(`Marked ${paths.length} ${noun} as Flat`, 'info', 2000);
    else showToast(`Reset ${changed} VR override${changed === 1 ? '' : 's'}`, 'info', 2000);
  }

  /**
   * Sync the video's in-memory funscript state (hasFunscript, funscriptPath,
   * _multiAxis, _customRouting) to reflect a given active mode. Clears the
   * slots that the new mode doesn't own so `_playFromLibrary` routes through
   * the correct path. Pass `config` for single=string, multi=object,
   * custom={routes}. Pass null for `active` to clear everything.
   */
  /** Lookup a scanned video record by its absolute path. Returns undefined if
   * the library hasn't scanned this path (e.g. source was removed). */
  getVideoByPath(videoPath) {
    if (!videoPath) return undefined;
    return this._videos.find(v => v.path === videoPath);
  }

  /**
   * Persist a freshly-downloaded script as a single-axis association for the
   * given videoPath and refresh the in-memory library entry so the badge
   * shows up on return to the library view. Preserves any existing multi/
   * custom slots. Returns true if the video was found and updated.
   */
  associateDownloadedScript(videoPath, scriptPath) {
    if (!videoPath || !scriptPath) return false;
    const video = this._videos.find(v => v.path === videoPath);
    if (!video) return false;

    const associations = this._settings.get('library.associations') || {};
    const entry = normalizeAssociation(associations[videoPath]);
    associations[videoPath] = buildAssociationEntry(
      'single',
      scriptPath,
      entry.multi,
      entry.custom,
    );
    this._settings.set('library.associations', associations);

    this._syncVideoStateForActive(video, 'single', scriptPath);
    this._applyFilters();
    return true;
  }

  _syncVideoStateForActive(video, active, config) {
    video._multiAxis = null;
    video._customRouting = null;
    if (active === 'single') {
      video.hasFunscript = true;
      video.funscriptPath = config;
      video._manualAssociation = true;
    } else if (active === 'multi') {
      const hasAnyScript = !!config.main || Object.values(config.axes || {}).some(Boolean);
      video.hasFunscript = hasAnyScript;
      video.funscriptPath = config.main || null;
      video._manualAssociation = true;
      video._multiAxis = config;
    } else if (active === 'custom') {
      const routes = config.routes || [];
      const mainRoute = routes.find(r => r.role === 'main');
      video.hasFunscript = !!mainRoute?.scriptPath;
      video.funscriptPath = mainRoute?.scriptPath || null;
      video._manualAssociation = true;
      video._customRouting = routes;
    } else {
      video.hasFunscript = false;
      video.funscriptPath = null;
      video._manualAssociation = false;
    }
  }

  /**
   * If the user is editing the currently-playing video's association, tear
   * down the app-level live routing state so the new active mode takes
   * effect immediately on the current playback instead of sticking with
   * whatever routing was loaded at play-time.
   */
  _tearDownLiveRoutingIfEditing(video) {
    if (!window.app || window.app._currentCustomRoutingVideoPath !== video.path) return;
    window.app._customRoutingActive = false;
    window.app._currentCustomRoutes = null;
    window.app._currentCustomRoutingVideoPath = null;
    if (window.app.buttplugSync) {
      window.app.buttplugSync._customRoutingActive = false;
      window.app.buttplugSync._axisAssignmentMap.clear();
      window.app.buttplugSync.clearAxisActions();
    }
    if (window.app.tcodeSync) {
      window.app.tcodeSync.clearAxisActions();
    }
  }

