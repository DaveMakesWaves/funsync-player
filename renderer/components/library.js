// Now that thumbnails are generated by the backend's ffmpeg (off-process)
// instead of hidden <video> elements in the renderer, the renderer no
// longer competes with the main player for GPU decode slots. The bottleneck
// is backend ffmpeg subprocess throughput, not GPU contention.
//
// Concurrency was 8 originally, lowered to 4 after a VR user reported
// startup timeouts on 6K/8K HEVC content: software HEVC decode at 8K
// runs ~5-10 fps, and 8 simultaneous decodes saturate a typical desktop
// CPU so badly that each subprocess exceeds its 60s timeout. Halving to
// 4 keeps per-decode CPU headroom while still parallelising the common
// H.264 case. If you raise this again, benchmark against an 8K HEVC
// sample (`Test.mp4` is H.264 1080p — won't catch the regression).
const MAX_CONCURRENT_THUMBNAILS = 4;

